package ders7;

public class Student {
	private String name;
	private int age;
	private int gpa;
	
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getAge() {
		return age;
	}
	public void setGpa(int gpa) {
		this.gpa = gpa;
	}
	public int getGpa() {
		return gpa;
	}
}
